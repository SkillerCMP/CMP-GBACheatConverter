std::pair<std::uint32_t, std::uint32_t>
tea_encrypt(std::uint32_t left, std::uint32_t right, const TeaKey& key) {
    std::uint32_t sum = 0;
    constexpr std::uint32_t delta = 0x9E3779B9U;

    for (int round = 0; round < 32; ++round) {
        sum += delta;
        left += ((right << 4U) + key[0]) ^
                (right + sum) ^
                ((right >> 5U) + key[1]);
        right += ((left << 4U) + key[2]) ^
                 (left + sum) ^
                 ((left >> 5U) + key[3]);
    }

    return {left, right};
}

std::pair<std::uint32_t, std::uint32_t>
tea_decrypt(std::uint32_t left, std::uint32_t right, const TeaKey& key) {
    constexpr std::uint32_t delta = 0x9E3779B9U;
    std::uint32_t sum = 0xC6EF3720U;

    for (int round = 0; round < 32; ++round) {
        right -= ((left << 4U) + key[2]) ^
                 (left + sum) ^
                 ((left >> 5U) + key[3]);
        left -= ((right << 4U) + key[0]) ^
                (right + sum) ^
                ((right >> 5U) + key[1]);
        sum -= delta;
    }

    return {left, right};
}

