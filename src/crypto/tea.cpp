std::uint32_t deadface_seed(
    const std::array<std::uint8_t, 256>& table1,
    const std::array<std::uint8_t, 256>& table2,
    std::uint8_t upper,
    std::uint8_t selector) {
    std::uint32_t result = 0;
    for (std::uint32_t index = 0; index < 4U; ++index) {
        const std::uint8_t byte = static_cast<std::uint8_t>(
            table1[(upper + index) & 0xFFU] + table2[selector]);
        result = (result << 8U) | byte;
    }
    return result;
}

TeaKey key_from_deadface(
    const std::array<std::uint8_t, 256>& table1,
    const std::array<std::uint8_t, 256>& table2,
    std::uint16_t value) {
    const std::uint8_t upper = static_cast<std::uint8_t>(value >> 8U);
    const std::uint8_t lower = static_cast<std::uint8_t>(value);
    TeaKey key{};
    for (std::uint32_t index = 0; index < key.size(); ++index) {
        key[index] = deadface_seed(
            table1,
            table2,
            upper,
            static_cast<std::uint8_t>(lower + index));
    }
    return key;
}

